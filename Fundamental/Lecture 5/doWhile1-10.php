<?php

/*---------------------------------------------------------------------
Напишете do-while цикъл, с който да отпечатате първите 10 нечетни числа,
започвайки от 1.
-----------------------------------------------------------------------*/


$number = 0;

do {
    if ($number % 2 != 0){   // if is diff from 0 is odd
        echo $number; // print the number
    }
    $number++;  // number = number + 1
} while ($number <= 10); // do till number <= 10