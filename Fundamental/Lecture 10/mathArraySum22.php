<?php

/*------------------------------------------------------------------------------
Създайте функция, която приема индексиран масив от числа като параметър и връща
сумата от всички числа в масива.1
---------------------------------------------------------------------------------*/


function sumArray($arr){
    $sum = 0;
    foreach ($arr as $num) {
        $sum += $num;
    }
    return $sum;
}

$arrayInput = [99, 98, 97, 96, 1];
echo sumArray($arrayInput);